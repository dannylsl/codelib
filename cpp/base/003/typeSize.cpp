#include <iostream>

using namespace std;

int main(void) {
	cout<<"Type char size ="<< sizeof(char) <<endl;		
	cout<<"Type int size ="<< sizeof(int) <<endl;		
	cout<<"Type short size ="<< sizeof(short) <<endl;		
	cout<<"Type long size ="<< sizeof(long) <<endl;		
	cout<<"Type long long size ="<< sizeof(long long) <<endl;		
	cout<<"===========================================" <<endl;
	cout<<"Type float size ="<< sizeof(float) <<endl;		
	cout<<"Type double size ="<< sizeof(double) <<endl;		

	return 0;
}

